def slice2d(x, start, end):
    return x[:, :, start:end, ...]


def slice3d(x, start, end):
    return x[:, :, :, start:end, ...]


def slice1d(x, start, end):
    return x[:, start:end, ...]


DIM_TO_SLICE = {
    1: slice1d,
    2: slice2d,
    3: slice3d,
}

class CustomKVCache:
    def __init__(
        self,
        start_size=128,
        local_size=128,
        middle_start=0,
        middle_end=0,
        k_seq_dim=2,
        v_seq_dim=2
    ):
        print(f"CustomKVCache: Start: {start_size}, Middle: {middle_start} to {middle_end}, LOCAL: {local_size}")
        self.start_size = start_size
        self.local_size = local_size
        self.middle_start = middle_start
        self.middle_end = middle_end
        self.cache_size = start_size + local_size + self.middle_end - self.middle_start
        self.k_seq_dim = k_seq_dim
        self.v_seq_dim = v_seq_dim
        self.k_slice = DIM_TO_SLICE[k_seq_dim]
        self.v_slice = DIM_TO_SLICE[v_seq_dim]

        assert self.cache_size > 0

    def __call__(self, past_key_values):

        if past_key_values is None:
            return None
        seq_len = past_key_values[0].size(self.k_seq_dim)
        if seq_len <= self.cache_size:
            return past_key_values

        self.middle_start = max(self.start_size, self.middle_start)
        self.middle_end = min(seq_len - self.local_size, self.middle_end)

        k, v = past_key_values[0], past_key_values[1]

        return [
                torch.cat(
                    [
                        self.k_slice(k, 0, self.start_size),
                        self.k_slice(k, self.middle_start, self.middle_end),
                        self.k_slice(k, seq_len - self.local_size, seq_len),
                    ],
                    dim=self.k_seq_dim,
                ),
                torch.cat(
                    [
                        self.v_slice(v, 0, self.start_size),
                        self.v_slice(v, self.middle_start, self.middle_end),
                        self.v_slice(v, seq_len - self.local_size, seq_len),
                    ],
                    dim=self.v_seq_dim,
                )
        ]



